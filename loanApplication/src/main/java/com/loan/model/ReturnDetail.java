package com.loan.model;



public class ReturnDetail {

	private String loanNo;
	private Integer interestAmt;
	private Integer actualIntAmt;
	
	
	public String getLoanNo() {
		return loanNo;
	}
	public void setLoanNo(String loanNo) {
		this.loanNo = loanNo;
	}
	public Integer getInterestAmt() {
		return interestAmt;
	}
	public void setInterestAmt(Integer interestAmt) {
		this.interestAmt = interestAmt;
	}
	
	public Integer getActualIntAmt() {
		return actualIntAmt;
	}
	public void setActualIntAmt(Integer actualIntAmt) {
		this.actualIntAmt = actualIntAmt;
	}
	
	
	
}
